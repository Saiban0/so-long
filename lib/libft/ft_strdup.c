#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*res;
	int		i;

	res = (char *)malloc(ft_strlen(s) + 1 * sizeof(char));
	if (!res)
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
